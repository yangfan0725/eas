    public void onLoad() throws Exception {
		super.onLoad();
		this.actionAddNew.setVisible(false);
		this.actionRemove.setVisible(false);
		this.actionView.setVisible(false);
		this.actionEdit.setVisible(false);
		int ProgressPayout = this.getUIContext().get("ProgressPayout")==null?16:Integer.parseInt(this.getUIContext().get("ProgressPayout").toString());
		FilterInfo filter = new FilterInfo();
		if(16==ProgressPayout){
			filter.getFilterItems().add(
					new FilterItemInfo("ProgressPayout", Boolean.FALSE,CompareType.EQUALS));
		}else{
			filter.getFilterItems().add(
					new FilterItemInfo("ProgressPayout", Boolean.TRUE,CompareType.EQUALS));
		}
		this.mainQuery.setFilter(filter);
		this.execQuery();
	}

