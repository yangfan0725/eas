	protected IObjectValue createNewData() {
		// TODO Auto-generated method stub
		AchievementStageInfo info = new AchievementStageInfo();
		IAchievementStage type = null;
		String num = null;
		try {
			// 得到最大编码
			// type = (IAchievementStage) getBizInterface();
			// num = type.getBiggestNumber();
			num = LbFacadeFactory.getRemoteInstance().getBiggestNumber3();
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		Integer n = Integer.valueOf(num);
		int m = n.intValue();
		String item = null;
		item = (m + 1) + "";
		if (item.length() < 2) {
			item = "0" + item;
		}
		info.setNumber(item);
		return info;
	}

	protected ICoreBase getBizInterface() throws Exception {
		// TODO Auto-generated method stub
		return AchievementStageFactory.getRemoteInstance();
	}

	// 只能输入字符型编码
	protected void verifyInput(ActionEvent e) throws Exception {
		// TODO Auto-generated method stub
		// super.verifyInput(e);
		if (FDCHelper.isEmpty(editData.getName())) {
			FDCMsgBox.showInfo("成果阶段名称不能为空");
			SysUtil.abort();
		}
		String num = this.txtNumber.getText();
		if (FDCHelper.isEmpty(num)) {
			FDCMsgBox.showInfo("成果阶段编码不能为空");
			SysUtil.abort();
		}
		boolean flag = num.matches("\\d+");
		// 判断输入的编码
		if (flag == false) {
			FDCMsgBox.showInfo("只能输入数字型编码");
			SysUtil.abort();
		}
	}
    
