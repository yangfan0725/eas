     public void initTree() throws Exception{
        this.treeMain.setModel(SHEHelper.getSellProjectTree(this.actionOnLoad,MoneySysTypeEnum.TenancySys));
 		this.treeMain.expandAllNodes(true, (TreeNode) this.treeMain.getModel().getRoot());
 		this.tblMain.getSelectManager().setSelectMode(KDTSelectManager.MULTIPLE_ROW_SELECT);
     }
     
     @Override
    protected String getEditUIName() {
    	// TODO Auto-generated method stub
    	return InvoiceBatchImportEditUI.class.getName();
    }
     
     protected void prepareUIContext(UIContext uiContext, ActionEvent e) {
 		super.prepareUIContext(uiContext, e);
 		uiContext.put(UIContext.ID, getSelectedKeyValue());
 		DefaultKingdeeTreeNode node = (DefaultKingdeeTreeNode) treeMain.getLastSelectedPathComponent();
 		if (node.getUserObject() instanceof SellProjectInfo) {
 			SellProjectInfo sellProject = (SellProjectInfo) node.getUserObject();
 			uiContext.put("sellProject", sellProject);
 		}
 	}
     
     protected IQueryExecutor getQueryExecutor(IMetaDataPK queryPK, EntityViewInfo viewInfo) {
 		try {
 			DefaultKingdeeTreeNode node = (DefaultKingdeeTreeNode) treeMain.getLastSelectedPathComponent();
 			viewInfo = (EntityViewInfo) this.mainQuery.clone();

 			FilterInfo filter = new FilterInfo();
 			if (node != null  &&  node.getUserObject() instanceof SellProjectInfo) {
 				SellProjectInfo pro = (SellProjectInfo) node.getUserObject();
 				filter.getFilterItems().add(new FilterItemInfo("sellProject.id", pro.getId().toString()));
 			} else {
 				filter.getFilterItems().add(new FilterItemInfo("id", null));
 			}

 			if (viewInfo.getFilter() != null) {
 				viewInfo.getFilter().mergeFilter(filter, "and");
 			} else {
 				viewInfo.setFilter(filter);
 			}
 		} catch (Exception e) {
 			handleException(e);
 		}

 		return super.getQueryExecutor(queryPK, viewInfo);
 	}
    
     
     
     @Override
    protected void treeMain_valueChanged(TreeSelectionEvent e) throws Exception {
    	// TODO Auto-generated method stub
    	super.treeMain_valueChanged(e);
    	DefaultKingdeeTreeNode node = (DefaultKingdeeTreeNode) treeMain.getLastSelectedPathComponent();
		if (node == null) {
			return;
		}
		SaleOrgUnitInfo saleOrg = SHEHelper.getCurrentSaleOrg();
		if(!saleOrg.isIsBizUnit())
		{
			this.actionAddNew.setEnabled(false);
			this.actionEdit.setEnabled(false);
			this.actionRemove.setEnabled(false);
			this.actionAudit.setEnabled(false);
			this.actionUnAudit.setEnabled(false);
		}else
		{
			this.actionAddNew.setEnabled(false);
			this.actionEdit.setEnabled(true);
			this.actionRemove.setEnabled(true);
			this.actionAudit.setEnabled(true);
			this.actionUnAudit.setEnabled(true);
			if(node.getUserObject() instanceof SellProjectInfo)
			{
				this.actionAddNew.setEnabled(true);				
			}
		}
		this.execQuery();
    }
     
     public BOSUuid getSelectedValue(){
    	 checkSelected();
    	 int rowIndex = this.tblMain.getSelectManager().getActiveRowIndex();
    	 String value  = this.tblMain.getCell(rowIndex, "id").getValue()+"";
    	 return BOSUuid.read(value);
    	 
     }
     
     @Override
    public void actionAudit_actionPerformed(ActionEvent e) throws Exception {
    	 InvoiceBatchImportFactory.getRemoteInstance().audit(getSelectedValue());
    	 this.refresh(e);
    }
     
     @Override
    public void actionUnAudit_actionPerformed(ActionEvent e) throws Exception {
    	 InvoiceBatchImportFactory.getRemoteInstance().unAudit(getSelectedValue());
    	 this.refresh(e);
    }
     
     
    
     

