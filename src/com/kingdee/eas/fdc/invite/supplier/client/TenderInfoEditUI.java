	@Override
	protected IObjectValue createNewData() {
		// TODO Auto-generated method stub
		//从列表页面带入招标立项和招标信息过来
		TenderInfoInfo info = new TenderInfoInfo();
		info.setNumber(UUID.randomUUID().toString());
		info.setName(UUID.randomUUID().toString());
		Map uiContext = getUIContext();
		if(uiContext.containsKey("inviteProject")){
			info.setInviteProject((InviteProjectInfo) uiContext.get("inviteProject"));
		}
		if(uiContext.containsKey("inviation")){
			info.setInvitation((InvitationInfoInfo) uiContext.get("invitaion"));
		}
		
		
		
		return info;
	}
	
