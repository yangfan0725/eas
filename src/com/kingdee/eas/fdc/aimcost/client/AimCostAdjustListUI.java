	public void onLoad() throws Exception {
		super.onLoad();
		boolean isAdjust = FDCUtils.getDefaultFDCParamByKey(null, null, FDCConstants.FDC_PARAM_AIMCOSTADJUSTINPUT);
		if(isAdjust){
			MsgBox.showWarning(this,"请先禁用参数 目标成本与调整记录是否允许直接录入!");
			SysUtil.abort();
		}
		tblMain.checkParsed();
		tblMain.getSelectManager().setSelectMode(KDTSelectManager.MULTIPLE_ROW_SELECT);
		tblMain.getDataRequestManager().setDataRequestMode(KDTDataRequestManager.REAL_MODE);
		initData();
		this.actionAudit.setEnabled(true);
		this.actionUnAudit.setEnabled(true);
	}

	private void initData() throws BOSException {
		tblMain.checkParsed();
		tblMain.removeRows();
		EntityViewInfo view = new EntityViewInfo();
		FilterInfo filter = new FilterInfo();
		view.setFilter(filter);
		view.getSelector().add("state");
		view.getSelector().add("number");
		view.getSelector().add("curProject.name");
		view.getSelector().add("creator.name");
		view.getSelector().add("createTime");
		view.getSelector().add("auditor.name");
		view.getSelector().add("auditTime");
		view.getSelector().add("id");
		//TODO 过滤工程项目ID
		Set idSet = this.getSelectObjLeafIds();
		filter.getFilterItems().add(new FilterItemInfo("curProject.id", idSet, CompareType.INCLUDE));
		AimCostAdjustCollection colls = AimCostAdjustFactory.getRemoteInstance().getAimCostAdjustCollection(view);
		if(colls!=null){
			IRow row = null;
			for(int i=0;i<colls.size();i++){
				AimCostAdjustInfo info = colls.get(i);
				row = this.tblMain.addRow();
				row.getCell("id").setValue(info.getId().toString());
				row.getCell("state").setValue(info.getState());
				row.getCell("number").setValue(info.getNumber());
				if(info.getCurProject()!=null){
					row.getCell("curProject.name").setValue(info.getCurProject().getName());
				}
				if(info.getCreator()!=null){
					row.getCell("creator.name").setValue(info.getCreator().getName());
				}
				row.getCell("createTime").setValue(info.getCreateTime());
				if(info.getAuditor()!=null){
					row.getCell("auditor.name").setValue(info.getAuditor().getName());
				}
				row.getCell("auditTime").setValue(info.getAuditTime());
			}
			
		}
//		this.tblMain.repaint();
//		this.setSelectFirstRow(tblMain);
		selectFirstRow();
	}
